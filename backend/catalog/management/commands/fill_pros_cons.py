class Command(BaseCommand):
    help = "Заполнить pros_text/cons_text у моделей кондиционеров"

    def add_arguments(self, parser):
        parser.add_argument(
            "--dry-run",
            action="store_true",
            help="Показать что будет записано, не трогая БД",
        )
        parser.add_argument(
            "--only",
            type=str,
            default=None,
            help="Применить только к одному slug",
        )

    def handle(self, *args, **options):
        dry_run: bool = options["dry_run"]
        only_slug: str | None = options["only"]

        updated = 0
        skipped = 0
        not_found = 0

        for slug, payload in DATA.items():
            if only_slug and slug != only_slug:
                continue
            if slug in SKIP_SLUGS:
                self.stdout.write(self.style.WARNING(f"SKIP  {slug}  (в skip-листе)"))
                skipped += 1
                continue

            pros_text = "\n".join(payload["pros"])
            cons_text = "\n".join(payload["cons"])

            try:
                model = ACModel.objects.get(slug=slug)
            except ACModel.DoesNotExist:
                self.stdout.write(self.style.ERROR(f"MISS  {slug}  (нет в БД)"))
                not_found += 1
                continue

            self.stdout.write(
                self.style.SUCCESS(f"OK    {slug}  ({model.brand.name} {model.inner_unit})")
            )
            self.stdout.write(f"      pros:\n        " + "\n        ".join(payload["pros"]))
            self.stdout.write(f"      cons:\n        " + "\n        ".join(payload["cons"]))

            if dry_run:
                updated += 1
                continue

            model.pros_text = pros_text
            model.cons_text = cons_text
            model.save(update_fields=["pros_text", "cons_text", "updated_at"])
            updated += 1

        self.stdout.write("")
        self.stdout.write(
            self.style.SUCCESS(
                f"Итого: updated={updated}, skipped={skipped}, not_found={not_found}"
                f"{' (DRY-RUN)' if dry_run else ''}"
            )
        )
